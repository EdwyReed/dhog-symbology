      <br/>
      <p class="font-manrope font-extrabold text-gray-blue text-body leading-normal mb-0">
        — И все эти трактовки могут быть верными, но не одновременно. И не для всех.
      </p>
      <br/>
      <p class="font-manrope font-extrabold text-gray-blue text-body leading-normal mb-0">
        — Символог не ищет «единственную истину». Символог ищет уместность.
      </p>
      <br/>
      <p class="font-manrope font-extrabold text-gray-blue text-body leading-normal mb-0">
        — А теперь главное.
      </p>
      <br/>
      <p class="font-manrope font-extrabold text-gray-blue text-body leading-normal mb-0">
        — В Символогии не проходит "я так думаю".<br/>
        Не проходит "мне приснилось".<br/>
        Не проходит "я где-то читал".
      </p>
      <br/>
      <p class="font-manrope font-extrabold text-gray-blue text-body leading-normal mb-0">
        Он кивает на потрёпанную книгу на кафедре.
      </p>
      <br/>
      <p class="font-manrope font-extrabold text-gray-blue text-body leading-normal mb-0">
        — Хотя читать всё равно полезно. Просто книга не думает за вас.
      </p>
      <br/>
      <p class="font-manrope font-extrabold text-gray-blue text-body leading-normal mb-0">
        Сайлас поворачивается к доске и пишет крупно:
      </p>
      <br/>
      <p class="font-manrope font-extrabold text-gray-blue text-body leading-normal mb-0">
        ДОКАЗАТЕЛЬСТВО = СВЯЗИ
      </p>
      <br/>
      <p class="font-manrope font-extrabold text-gray-blue text-body leading-normal mb-0">
        Он подчёркивает слово "связи" дважды.
      </p>
      <br/>
      <p class="font-manrope font-extrabold text-gray-blue text-body leading-normal mb-0">
        — Вы доказываете трактовку не красотой речи. Вы доказываете её тем, что она держится на месте.<br/>
        Связями символа с тем, где он стоит.<br/>
        С тем, кто его оставил.<br/>
        С тем, что он запускает.<br/>
        С тем, что меняется после него.
      </p>
      <br/>
      <p class="font-manrope font-extrabold text-gray-blue text-body leading-normal mb-0">
        Сайлас отходит от доски и начинает ходить между рядами, глядя на студентов поверх очков-пустышек.
      </p>
      <br/>
      <p class="font-manrope font-extrabold text-gray-blue text-body leading-normal mb-0">
        — Говорите "это защита"? Отлично. Тогда покажите границу.<br/>
        Говорите "это память"? Тогда покажите, что именно он удерживает.<br/>
        Говорите "это переход"? Тогда покажите "до" и "после" или "откуда" и "куда".
      </p>
      <br/>
      <p class="font-manrope font-extrabold text-gray-blue text-body leading-normal mb-0">
        Он останавливается у ближайшей парты.
      </p>
      <br/>
      <p class="font-manrope text-gray-blue text-body leading-normal mb-0">
        <span class="font-extrabold">— Если ваша трактовка ничего не меняет, она пустая.<br/>
        А если меняет слишком многое…</span>
        — Сайлас улыбается. —
        <span class="font-extrabold">поздравляю. Вы нашли символ, который кому-то захочется спрятать.</span>
      </p>
      <br/>
      <p class="font-manrope font-extrabold text-gray-blue text-body leading-normal mb-0">
        И последнее.
      </p>
      <br/>
      <p class="font-manrope font-extrabold text-gray-blue text-body leading-normal mb-0">
        Сайлас стирает доску одним движением. Пыль от мела оседает на пальцах.
      </p>
      <br/>
      <p class="font-manrope font-extrabold text-gray-blue text-body leading-normal mb-0">
        — Символ не нейтрален.<br/>
        Символ — это не предмет. Это связь. Круг на бумаге всегда будет просто кругом. Но круг, начертанный солью вокруг дома с намерением защитить — уже символ. Именно поэтому мы начинаем с формы, но не останавливаемся на ней.
      </p>
      <br/>
      <p class="font-manrope font-extrabold text-gray-blue text-body leading-normal mb-0">
        Он кладёт ладонь на край кафедры и смотрит на аудиторию прямо.
      </p>
      <br/>
      <p class="font-manrope font-extrabold text-gray-blue text-body leading-normal mb-0">
        — Вы не просто смотрите на символ.<br/>
        Вы входите с ним в контакт.<br/>
        И иногда он отвечает.
      </p>
      <br/>
      <p class="font-manrope font-extrabold text-gray-blue text-body leading-normal mb-0">
        Сайлас делает короткую паузу.
      </p>
      <br/>
      <p class="font-manrope font-extrabold text-gray-blue text-body leading-normal mb-0">
        — На следующей лекции мы перейдём к частным системам — там, где символы быстрее всего начинают свою : стихии, вещества, пространство.
      </p>
      <br/>
      <p class="font-manrope font-extrabold text-gray-blue text-body leading-normal mb-0">
        Он поправляет мантию, берёт книгу со стола.
      </p>
      <br/>
      <p class="font-manrope font-extrabold text-gray-blue text-body leading-normal mb-0">
        — А пока просьба на сегодня простая: не называйте гербы "картинками".<br/>
        Это обидно. И, честно говоря, опасно.
      </p>
      <br/>
      <p class="font-manrope font-extrabold text-gray-blue text-body leading-normal mb-0">
        Сайлас улыбается и кивает на дверь:
      </p>
      <br/>
      <p class="font-manrope font-extrabold text-gray-blue text-body leading-normal mb-0">
        — Свободны.
      </p>
